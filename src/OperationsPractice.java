
public class OperationsPractice {

	public static void main(String  []args) {
		
		int number;  //Data type, then variable name.
		number = 7;
		System.out.println(number);
		number += 5;
		System.out.print("+ 5: ");
		System.out.println(number);
		number /= 2;
		System.out.print("/ 2: ");
		System.out.println(number);
		number *= 4;
		System.out.print("* 4: ");
		System.out.println(number);
		number -= 6;
		System.out.print("- 6: ");
		System.out.println(number);
		number %= 2;
		System.out.print("% 2: ");
		System.out.println(number);
		
	}
	
}
